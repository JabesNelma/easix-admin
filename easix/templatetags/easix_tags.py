@register.filter
def badge_class(value, badge_map=None):
    """Get badge CSS class based on value."""
    if badge_map is None:
        badge_map = {
            True: "success",
            False: "danger",
            "active": "success",
            "inactive": "danger",
            "published": "success",
            "draft": "warning",
            "pending": "warning",
            "approved": "success",
            "rejected": "danger",
        }
    
    key = str(value).lower() if value is not None else ""
    return badge_map.get(key, badge_map.get(str(value), "gray"))


@register.filter
def format_value(value, format_type=None):
    """Format a value for display."""
    if value is None:
        return ""
    
    if format_type == "date":
        return value.strftime("%b %d, %Y") if hasattr(value, "strftime") else str(value)
    
    if format_type == "datetime":
        return value.strftime("%b %d, %Y %H:%M") if hasattr(value, "strftime") else str(value)
    
    if format_type == "currency":
        try:
            return f"${float(value):,.2f}"
        except (ValueError, TypeError):
            return str(value)
    
    if format_type == "number":
        try:
            return f"{float(value):,}"
        except (ValueError, TypeError):
            return str(value)
    
    if format_type == "boolean":
        return "Yes" if value else "No"
    
    if format_type == "file_size":
        try:
            bytes_val = int(value)
            if bytes_val == 0:
                return "0 Bytes"
            sizes = ["Bytes", "KB", "MB", "GB", "TB"]
            import math
            i = int(math.floor(math.log(bytes_val, 1024)))
            return f"{round(bytes_val / math.pow(1024, i), 2)} {sizes[i]}"
        except (ValueError, TypeError):
            return str(value)
    
    return str(value)


@register.filter
def truncate_words(value, num=10):
    """Truncate text to a number of words."""
    if not value:
        return ""
    words = str(value).split()
    if len(words) <= num:
        return value
    return " ".join(words[:num]) + "..."


@register.filter
def add_class(field, css_class):
    """Add CSS class to a form field."""
    if hasattr(field, "as_widget"):
        return field.as_widget(attrs={"class": css_class})
    return field


@register.filter
def get_item(dictionary, key):
    """Get item from dictionary."""
    if dictionary is None:
        return None
    return dictionary.get(key)


@register.filter
def multiply(value, arg):
    """Multiply two numbers."""
    try:
        return float(value) * float(arg)
    except (ValueError, TypeError):
        return 0


@register.filter
def divide(value, arg):
    """Divide two numbers."""
    try:
        return float(value) / float(arg)
    except (ValueError, TypeError, ZeroDivisionError):
        return 0


