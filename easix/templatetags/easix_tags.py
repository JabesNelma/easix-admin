@register.inclusion_tag("easix/components/badge.html", takes_context=True)
def badge_tag(context, value, style="default"):
    """Render a badge."""
    styles = {
        "default": "bg-gray-100 text-gray-800",
        "primary": "bg-indigo-100 text-indigo-800",
        "success": "bg-green-100 text-green-800",
        "danger": "bg-red-100 text-red-800",
        "warning": "bg-yellow-100 text-yellow-800",
        "info": "bg-blue-100 text-blue-800",
    }
    
    # Auto-detect style from value
    if style == "auto":
        if value is True or str(value).lower() in ["active", "published", "approved", "yes"]:
            style = "success"
        elif value is False or str(value).lower() in ["inactive", "draft", "rejected", "no"]:
            style = "danger"
        elif str(value).lower() in ["pending", "processing"]:
            style = "warning"
        else:
            style = "default"
    
    return {
        "value": value,
        "class": styles.get(style, styles["default"]),
    }


